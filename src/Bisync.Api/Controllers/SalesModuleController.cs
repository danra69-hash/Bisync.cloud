    static readonly JsonSerializerOptions JsonOpts = new() { PropertyNamingPolicy = JsonNamingPolicy.CamelCase };

    static readonly HashSet<string> AllowedStatuses = new(StringComparer.OrdinalIgnoreCase)
    {
        "Prospect", "Engaged", "Active", "Inactive", "Closed",
    };

    static readonly HashSet<string> AllowedDiaryStatuses = new(StringComparer.OrdinalIgnoreCase)
    {
        "Prospect", "Proposed", "In Nego", "Sales Order", "Invoiced", "Paid", "Activated", "KIV", "Rejected",
    };

    static readonly HashSet<string> AllowedDiaryContactTypes = new(StringComparer.OrdinalIgnoreCase)
    {
        "Cold Call", "Email Blast", "Follow up Call", "Follow up Visit", "Follow Up email",
        "Sales Meeting", "Demo Meeting", "Follow Up Meeting", "Online Presentation", "Offline Presentation",
        "Training", "Retraining", "Post Meeting", "Regular Catch Up",
    };

    static string NormalizeDiaryActivity(string? raw)
    {
        var s = (raw ?? string.Empty).Trim().Replace(" ", "", StringComparison.Ordinal);
        if (s.Equals("StatusChange", StringComparison.OrdinalIgnoreCase)) return "StatusChange";
        if (s.Equals("SalesCall", StringComparison.OrdinalIgnoreCase)) return "SalesCall";
        return string.Empty;
    }

    static string? ValidateDiaryEntry(CreateSalesModuleDiaryEntryRequest request)
    {
        if (request.SalesTeamMemberId <= 0) return "salesTeamMemberId is required.";
        var activity = NormalizeDiaryActivity(request.ActivityType);
        if (string.IsNullOrEmpty(activity)) return "Activity must be Status Change or Sales Call.";
        if (request.ContactDate == default) return "Date of Contact is required.";
        if (activity == "SalesCall")
        {
            var contactType = (request.ContactType ?? string.Empty).Trim();
            if (string.IsNullOrWhiteSpace(contactType) || !AllowedDiaryContactTypes.Contains(contactType))
                return "Select a valid Type of Contact.";
        }
        return null;
    }

    async Task EnsureSalesDiarySchemaAsync(CancellationToken ct)
    {
        await db.Database.ExecuteSqlRawAsync("""
            CREATE TABLE IF NOT EXISTS "SalesModuleDiaryEntries" (
                "Id" integer GENERATED BY DEFAULT AS IDENTITY NOT NULL CONSTRAINT "PK_SalesModuleDiaryEntries" PRIMARY KEY,
                "SalesTeamMemberId" integer NOT NULL,
                "ActivityType" TEXT NOT NULL DEFAULT '',
                "SalesModuleCompanyId" integer NULL,
                "CompanyName" TEXT NOT NULL DEFAULT '',
                "BrandName" TEXT NOT NULL DEFAULT '',
                "LocationVisited" TEXT NOT NULL DEFAULT '',
                "EmailsSent" integer NULL,
                "StatusesJson" TEXT NOT NULL DEFAULT '[]',
                "ContactType" TEXT NOT NULL DEFAULT '',
                "ContactDate" timestamp with time zone NOT NULL DEFAULT NOW(),
                "ContactsJson" TEXT NOT NULL DEFAULT '[]',
                "CreatedAt" timestamp with time zone NOT NULL DEFAULT NOW(),
                "CreatedByEmail" TEXT NOT NULL DEFAULT ''
            );
            """, ct);
        await db.Database.ExecuteSqlRawAsync("""
            CREATE INDEX IF NOT EXISTS "IX_SalesModuleDiaryEntries_Member"
            ON "SalesModuleDiaryEntries" ("SalesTeamMemberId");
            """, ct);
        await db.Database.ExecuteSqlRawAsync("""
            CREATE INDEX IF NOT EXISTS "IX_SalesModuleDiaryEntries_ContactDate"
            ON "SalesModuleDiaryEntries" ("ContactDate");
            """, ct);
    }

    static object MapDiaryEntry(SalesModuleDiaryEntry e)
    {
        List<string> statuses;
        List<object> contacts;
        try
        {
            statuses = JsonSerializer.Deserialize<List<string>>(e.StatusesJson, JsonOpts) ?? [];
        }
        catch
        {
            statuses = [];
        }
        try
        {
            contacts = JsonSerializer.Deserialize<List<object>>(e.ContactsJson, JsonOpts) ?? [];
        }
        catch
        {
            contacts = [];
        }

        return new
        {
            e.Id,
            e.SalesTeamMemberId,
            activityType = e.ActivityType,
            activityLabel = e.ActivityType == "StatusChange" ? "Status Change" : "Sales Call",
            salesModuleCompanyId = e.SalesModuleCompanyId,
            companyName = e.CompanyName,
            brandName = e.BrandName,
            locationVisited = e.LocationVisited,
            emailsSent = e.EmailsSent,
            statuses,
            contactType = e.ContactType,
            contactDate = e.ContactDate,
            contacts,
            createdAt = e.CreatedAt,
            createdByEmail = e.CreatedByEmail,
        };
    }

    [HttpGet("customers")]
    public async Task<ActionResult<IEnumerable<object>>> GetCustomers(
        [FromQuery] int? companyId = null,
        [FromQuery] int? salesTeamMemberId = null,
        [FromQuery] int? engagedUserId = null,
        CancellationToken ct = default)
    {
        await EnsureSalesCompanySchemaAsync(ct);

        HashSet<int>? allowedCompanyIds = null;
        if (salesTeamMemberId is > 0)
        {
            var ids = await db.SalesModuleCompanyMembers.AsNoTracking()
                .Where(m => m.SalesTeamMemberId == salesTeamMemberId.Value)
                .Select(m => m.SalesModuleCompanyId)
                .ToListAsync(ct);
            allowedCompanyIds = ids.ToHashSet();
        }

        if (companyId is > 0)
        {
            if (allowedCompanyIds is not null && !allowedCompanyIds.Contains(companyId.Value))
                return Ok(Array.Empty<object>());
            allowedCompanyIds = new HashSet<int> { companyId.Value };
        }

        if (allowedCompanyIds is null)
            return BadRequest(new { message = "companyId or salesTeamMemberId is required." });

        if (allowedCompanyIds.Count == 0)
            return Ok(Array.Empty<object>());

        var q = db.SalesModuleCustomers.AsNoTracking()
            .Where(c => c.Active && allowedCompanyIds.Contains(c.CompanyId));
        if (engagedUserId is > 0)
            q = q.Where(c => c.EngagedUserId == engagedUserId.Value);

        var rows = await q.OrderByDescending(c => c.CreatedAt).ToListAsync(ct);
        return Ok(rows.Select(MapCustomer));
    }

    [HttpPost("customers")]
    public async Task<ActionResult<object>> CreateCustomer([FromBody] UpsertSalesModuleCustomerRequest request, CancellationToken ct)
    {
        var error = ValidateCustomer(request);
        if (error is not null) return BadRequest(new { message = error });

        await EnsureSalesCompanySchemaAsync(ct);
        var salesCompany = await db.SalesModuleCompanies.AsNoTracking()
            .FirstOrDefaultAsync(c => c.Id == request.CompanyId && c.Active, ct);
        if (salesCompany is null)
            return BadRequest(new { message = "Sales company not found. Create and tag a company to a sales team member first." });

        var externalId = string.IsNullOrWhiteSpace(request.ExternalId)
            ? await NextCustomerExternalIdAsync(request.CompanyId, ct)
            : request.ExternalId.Trim();

        if (await db.SalesModuleCustomers.AnyAsync(c => c.ExternalId.ToLower() == externalId.ToLower(), ct))
            return Conflict(new { message = $"Customer id {externalId} already exists." });

        var customer = MapToCustomer(new SalesModuleCustomer(), request, externalId);
        if (string.IsNullOrWhiteSpace(customer.CompanyName))
            customer.CompanyName = salesCompany.Name;
        customer.CreatedAt = DateTime.UtcNow;
        db.SalesModuleCustomers.Add(customer);
        await db.SaveChangesAsync(ct);
        return Ok(MapCustomer(customer));
    }

    [HttpPut("customers/{externalId}")]
