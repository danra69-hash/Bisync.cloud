    /// <summary>Create a Sales Diary Status Change or Sales Call entry.</summary>
    [HttpPost("diary")]
    public async Task<ActionResult<object>> CreateDiaryEntry(
        [FromBody] CreateSalesModuleDiaryEntryRequest request,
        CancellationToken ct = default)
    {
        await EnsureSalesDiarySchemaAsync(ct);
        await EnsureSalesCompanySchemaAsync(ct);

        var error = ValidateDiaryEntry(request);
        if (error is not null) return BadRequest(new { message = error });

        var member = await db.SalesModuleTeamMembers.AsNoTracking()
            .FirstOrDefaultAsync(m => m.Id == request.SalesTeamMemberId, ct);
        if (member is null)
            return BadRequest(new { message = "Sales team member not found." });

        var activity = NormalizeDiaryActivity(request.ActivityType);
        var contactType = (request.ContactType ?? string.Empty).Trim();
        var skipsCompany = activity == "SalesCall"
            && (contactType.Equals("Cold Call", StringComparison.OrdinalIgnoreCase)
                || contactType.Equals("Email Blast", StringComparison.OrdinalIgnoreCase));

        int? companyId = null;
        var companyName = (request.CompanyName ?? string.Empty).Trim();

        if (!skipsCompany)
        {
            if (request.SalesModuleCompanyId is not > 0)
                return BadRequest(new { message = "Select a company tagged to this Hunter." });

            var tagged = await db.SalesModuleCompanyMembers.AsNoTracking()
                .AnyAsync(
                    t => t.SalesModuleCompanyId == request.SalesModuleCompanyId.Value
                        && t.SalesTeamMemberId == request.SalesTeamMemberId,
                    ct);
            if (!tagged)
                return BadRequest(new { message = "Company is not tagged to this Hunter." });

            var company = await db.SalesModuleCompanies.AsNoTracking()
                .FirstOrDefaultAsync(c => c.Id == request.SalesModuleCompanyId.Value && c.Active, ct);
            if (company is null)
                return BadRequest(new { message = "Company not found." });

            companyId = company.Id;
            companyName = company.Name;
        }
        else if (contactType.Equals("Cold Call", StringComparison.OrdinalIgnoreCase))
        {
            if (string.IsNullOrWhiteSpace(companyName) && string.IsNullOrWhiteSpace(request.BrandName))
                return BadRequest(new { message = "Cold Call requires company / brand name." });
            if (string.IsNullOrWhiteSpace(request.LocationVisited))
                return BadRequest(new { message = "Cold Call requires location visited." });
            if (string.IsNullOrWhiteSpace(companyName))
                companyName = (request.BrandName ?? string.Empty).Trim();
        }
        else if (contactType.Equals("Email Blast", StringComparison.OrdinalIgnoreCase))
        {
            if (request.EmailsSent is null or < 1)
                return BadRequest(new { message = "Email Blast requires number of emails sent." });
        }

        var contacts = (request.Contacts ?? [])
            .Where(c => !string.IsNullOrWhiteSpace(c.Name))
            .Select(c => new
            {
                name = c.Name.Trim(),
                position = (c.Position ?? string.Empty).Trim(),
            })
            .ToList();

        if (activity == "SalesCall" && contacts.Count == 0
            && !contactType.Equals("Email Blast", StringComparison.OrdinalIgnoreCase))
        {
            // Contact person required for sales calls except email blast (mass send).
            // User asked for Contact Person / Position with + button — require at least one name for non-blast.
            return BadRequest(new { message = "Add at least one contact person." });
        }

        var statuses = activity == "StatusChange"
            ? (request.Statuses ?? [])
                .Select(s => s.Trim())
                .Where(s => AllowedDiaryStatuses.Contains(s))
                .Distinct(StringComparer.OrdinalIgnoreCase)
                .ToList()
            : [];

        if (activity == "StatusChange" && statuses.Count == 0)
            return BadRequest(new { message = "Tick at least one status." });

        var row = new SalesModuleDiaryEntry
        {
            SalesTeamMemberId = request.SalesTeamMemberId,
            ActivityType = activity,
            SalesModuleCompanyId = companyId,
            CompanyName = companyName,
            BrandName = (request.BrandName ?? string.Empty).Trim(),
            LocationVisited = (request.LocationVisited ?? string.Empty).Trim(),
            EmailsSent = contactType.Equals("Email Blast", StringComparison.OrdinalIgnoreCase)
                ? request.EmailsSent
                : null,
            StatusesJson = JsonSerializer.Serialize(statuses, JsonOpts),
            ContactType = activity == "SalesCall" ? contactType : string.Empty,
            ContactDate = DateTime.SpecifyKind(request.ContactDate.Date, DateTimeKind.Utc),
            ContactsJson = JsonSerializer.Serialize(contacts, JsonOpts),
            CreatedAt = DateTime.UtcNow,
            CreatedByEmail = (request.CreatedByEmail ?? string.Empty).Trim().ToLowerInvariant(),
        };
        db.SalesModuleDiaryEntries.Add(row);
        await db.SaveChangesAsync(ct);

        // Keep Client Update (and Overview metrics) aligned with diary activity.
        var contactTuples = contacts
            .Select(c => (c.name, c.position))
            .ToList();
        try
        {
            await clientUpdateService.ApplyDiaryEntryAsync(
                member.Id,
                member.Name,
                activity,
                companyName,
                row.BrandName,
                row.LocationVisited,
                row.EmailsSent,
                statuses,
                row.ContactType,
                row.ContactDate,
                contactTuples,
                ct);
        }
        catch
        {
            // Diary is already saved; do not fail the request if Client Update sync fails.
        }

        return Ok(MapDiaryEntry(row));
    }

